package com.miaoyu.naigos.model;

public class ThemeIdSubcategoryModel {
    private String subcategory;
    private String category_value;
    private String cover_image;
    private String zhsg_name;

    public String getSubcategory() {
        return subcategory;
    }

    public void setSubcategory(String subcategory) {
        this.subcategory = subcategory;
    }

    public String getCategory_value() {
        return category_value;
    }

    public void setCategory_value(String category_value) {
        this.category_value = category_value;
    }

    public String getCover_image() {
        return cover_image;
    }

    public void setCover_image(String cover_image) {
        this.cover_image = cover_image;
    }

    public String getZhsg_name() {
        return zhsg_name;
    }

    public void setZhsg_name(String zhsg_name) {
        this.zhsg_name = zhsg_name;
    }
}
